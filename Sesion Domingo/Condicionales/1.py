#Convertir un valor numerico a texto


while (True):
    numero = int(input("Digite un valor entre 1 y 3\n"))

    if numero == 1:
        print("uno")
        break
    elif numero == 2:
        print("dos")
        break
    elif numero == 3:
        print("tres")
        break
    else:
        print("digite una opcion valida")
        break